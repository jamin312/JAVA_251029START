public class 데이터타입 {

	public static void main(String[] args) {
	
		// 반려견의 이름을 name 변수에 저장하세요
		String name = "여름";
		
		// 반려견의 나이를 age 변수에 저장하세요
		long age = 5;
		
		// 반려견의 몸무게를 weight 변수에 저장하세요
		double weight = 3.8;
		
		System.out.println("나의 반려견 이름은 :" + name);
		System.out.println("나의 반려견 나이는 :" + age + "세");
		System.out.println("나의 반려견 몸무게는 :" + weight + "kg");
		
		
	}
}
